import { 
  createUserWithEmailAndPassword,
  signInWithEmailAndPassword,
  signOut,
  updateProfile,
  onAuthStateChanged,
  sendPasswordResetEmail,
  GoogleAuthProvider,
  signInWithPopup
} from 'firebase/auth'
import { 
  doc, 
  setDoc, 
  getDoc, 
  updateDoc 
} from 'firebase/firestore'
import { auth, db } from './firebase'

// Provider do Google
const googleProvider = new GoogleAuthProvider()
googleProvider.setCustomParameters({
  prompt: 'select_account'
})

/**
 * Registra um novo usuário
 */
export const registerUser = async (email, password, fullName) => {
  try {
    // Criar usuário no Firebase Auth
    const userCredential = await createUserWithEmailAndPassword(auth, email, password)
    const user = userCredential.user

    // Atualizar o displayName no Auth
    await updateProfile(user, {
      displayName: fullName
    })

    // Criar documento do usuário no Firestore
    await setDoc(doc(db, 'users', user.uid), {
      uid: user.uid,
      email: user.email,
      fullName: fullName,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    })

    return { user, error: null }
  } catch (error) {
    console.error('Erro ao registrar usuário:', error)
    return { user: null, error: error.message }
  }
}

/**
 * Faz login do usuário
 */
export const loginUser = async (email, password) => {
  try {
    const userCredential = await signInWithEmailAndPassword(auth, email, password)
    return { user: userCredential.user, error: null }
  } catch (error) {
    console.error('Erro ao fazer login:', error)
    let errorMessage = 'Erro ao fazer login. Tente novamente.'
    
    if (error.code === 'auth/user-not-found') {
      errorMessage = 'Usuário não encontrado. Verifique o email ou crie uma conta.'
    } else if (error.code === 'auth/wrong-password') {
      errorMessage = 'Senha incorreta. Tente novamente.'
    } else if (error.code === 'auth/invalid-email') {
      errorMessage = 'Email inválido. Verifique o formato do email.'
    } else if (error.code === 'auth/too-many-requests') {
      errorMessage = 'Muitas tentativas. Aguarde um momento e tente novamente.'
    } else if (error.code === 'auth/invalid-credential') {
      errorMessage = 'Email ou senha incorretos. Verifique e tente novamente.'
    }
    
    return { user: null, error: errorMessage }
  }
}

/**
 * Faz login com Google
 */
export const loginWithGoogle = async () => {
  try {
    const result = await signInWithPopup(auth, googleProvider)
    const user = result.user
    
    // Verificar se o usuário já existe no Firestore
    const userDoc = await getDoc(doc(db, 'users', user.uid))
    
    if (!userDoc.exists()) {
      // Criar documento do usuário no Firestore (primeiro login com Google)
      await setDoc(doc(db, 'users', user.uid), {
        uid: user.uid,
        email: user.email,
        fullName: user.displayName || '',
        photoURL: user.photoURL || '',
        provider: 'google',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString()
      })
    }
    
    return { user, error: null }
  } catch (error) {
    console.error('Erro ao fazer login com Google:', error)
    let errorMessage = 'Erro ao fazer login com Google. Tente novamente.'
    
    if (error.code === 'auth/popup-closed-by-user') {
      errorMessage = 'Login cancelado. A janela foi fechada.'
    } else if (error.code === 'auth/popup-blocked') {
      errorMessage = 'Popup bloqueado. Permita popups para este site.'
    } else if (error.code === 'auth/cancelled-popup-request') {
      errorMessage = 'Login cancelado.'
    }
    
    return { user: null, error: errorMessage }
  }
}

/**
 * Faz logout do usuário
 */
export const logoutUser = async () => {
  try {
    await signOut(auth)
    // Limpar localStorage ao fazer logout
    localStorage.removeItem('userProfile')
    return { error: null }
  } catch (error) {
    console.error('Erro ao fazer logout:', error)
    return { error: error.message }
  }
}

/**
 * Atualiza o nome completo do usuário
 */
export const updateUserFullName = async (userId, fullName) => {
  try {
    // Validar se o nome é completo (tem pelo menos 2 palavras)
    const nameParts = fullName.trim().split(/\s+/)
    if (nameParts.length < 2) {
      return { error: 'Por favor, informe seu nome completo (nome e sobrenome)' }
    }

    const user = auth.currentUser
    if (!user) {
      return { error: 'Usuário não autenticado' }
    }

    // Atualizar no Auth
    await updateProfile(user, {
      displayName: fullName
    })

    // Atualizar no Firestore
    await updateDoc(doc(db, 'users', userId), {
      fullName: fullName,
      updatedAt: new Date().toISOString()
    })

    return { error: null }
  } catch (error) {
    console.error('Erro ao atualizar nome:', error)
    return { error: error.message }
  }
}

/**
 * Atualiza dados do perfil do usuário
 */
export const updateUserProfile = async (userId, profileData) => {
  try {
    const user = auth.currentUser
    if (!user) {
      return { error: 'Usuário não autenticado' }
    }

    // Atualizar no Firestore
    await updateDoc(doc(db, 'users', userId), {
      ...profileData,
      updatedAt: new Date().toISOString()
    })

    return { error: null }
  } catch (error) {
    console.error('Erro ao atualizar perfil:', error)
    return { error: error.message }
  }
}

/**
 * Busca dados do usuário no Firestore
 */
export const getUserData = async (userId) => {
  try {
    const userDoc = await getDoc(doc(db, 'users', userId))
    if (userDoc.exists()) {
      return { data: userDoc.data(), error: null }
    }
    return { data: null, error: 'Usuário não encontrado' }
  } catch (error) {
    // Silenciar erros de permissão - as regras do Firestore precisam ser configuradas
    if (error.code !== 'permission-denied') {
      console.error('Erro ao buscar dados do usuário:', error)
    }
    return { data: null, error: error.message }
  }
}

/**
 * Salva dados do perfil no localStorage
 */
export const saveProfileToLocalStorage = (userId, profileData) => {
  try {
    const data = {
      userId,
      ...profileData,
      savedAt: new Date().toISOString()
    }
    localStorage.setItem('userProfile', JSON.stringify(data))
    return { error: null }
  } catch (error) {
    console.error('Erro ao salvar no localStorage:', error)
    return { error: error.message }
  }
}

/**
 * Carrega dados do perfil do localStorage
 */
export const loadProfileFromLocalStorage = (userId) => {
  try {
    const data = localStorage.getItem('userProfile')
    if (data) {
      const parsed = JSON.parse(data)
      // Verificar se é do mesmo usuário
      if (parsed.userId === userId) {
        return { data: parsed, error: null }
      }
    }
    return { data: null, error: null }
  } catch (error) {
    console.error('Erro ao carregar do localStorage:', error)
    return { data: null, error: error.message }
  }
}

/**
 * Converte arquivo de imagem para base64
 */
export const imageToBase64 = (file) => {
  return new Promise((resolve, reject) => {
    const reader = new FileReader()
    reader.onload = () => resolve(reader.result)
    reader.onerror = (error) => reject(error)
    reader.readAsDataURL(file)
  })
}

